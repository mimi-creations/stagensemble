select * from (SELECT 
    2 num_ligne,
    'C09'        AS TYPE_ENTITE,
    'CP_ID'      AS IDENTIFIANT_CONTREPARTIE_RACI_BCI,
    'TYP_CP_ID'  AS TYP_IDENTIFIANT,
    'DATE_DECL'  AS DATE_DECLARATION,
    'C02'        AS IDENTIFIANT_NAT_PERSONNE_PHY,
    'TYP_C02'    AS TYPE_IDENTIFIANT_NAT_PERSONNE_PHY,
    'C01'        AS IDENTIFIANT_NATIONAL_ENTREPRISE,
    'TYP_C01'    AS TYPE_IDENTIFIANT_NATIONAL_ENTREPRISE,
    'C03'        AS IDENTIFIANT_ENTITE_JURIDIQUE,
    'C06'        AS IDENTIFIANT_ENTREPRISE_SIEGE,
    'TYP_C06'    AS TYPE_IDENTIFIANT_SG,
    'C07'        AS IDENTIFIANT_ENTREPRISE_MERE,
    'TYP_C07'    AS TYPE_IDENTIFIANT_MI,
    'C08'        AS IDENTIFIANT_ENTREPRISE_MERE_ULTIME,
    'TYP_C08'    AS TYPE_IDENTIFIANT_M_U,
    'C12'        AS NOM_FAMILLE,
    'C13'        AS PRENOM,
    'C14'        AS DATE_NAISSANCE,
    'C17'        AS ADRESSE_RUE,
    'C18'        AS ADRESSE_NUMERO,
    'C19'        AS ADRESSE_VILLE,
    'C20'        AS DIVISION_ADMINISTRATIVE,
    'C21'        AS CODE_POSTAL,
    'C22'        AS PAYS,
    'C11'        AS ENTITE_JURIDIQUE_NOM,
    'C23'        AS FORME_JURIDIQUE,
    'C24'        AS SECTEUR_INSTITUTIONNEL,
    'C25'        AS ACTIVITE_ECONOMIQUE,
    'C26'        AS ETAT_AVANCEMENT_PROCED_JUD,
    'C27'        AS DATE_OUVERTURE_PROCED_JUD,
    'C28'        AS TAILLE_ENTREPRISE,
    'C29'        AS DATE_TAILLE_ENTREPRISE,
    'C30'        AS NOMBRE_SALARIES,
    'C31'        AS TOTAL_BILAN,
    'C32'        AS CHIFFRE_AFFAIRES_ANNUEL,
    'REF_XLS'    AS LIGNE_EXCEL,
    'REF_AUDIT'  AS LIGNE_AUDIT,
    0            AS SORT_ORDER
FROM DUAL

UNION ALL

SELECT 
LIGNE_EXCEL+1 num_ligne,
    TYPE_ENTITE,
    IDENTIFIANT_CONTREPARTIE_RACI_BCI,
    TYP_IDENTIFIANT,
    TO_CHAR(DATE_DECLARATION, 'DD/MM/YYYY'),
    IDENTIFIANT_NAT_PERSONNE_PHY,
    TYPE_IDENTIFIANT_NAT_PERSONNE_PHY,
    IDENTIFIANT_NATIONAL_ENTREPRISE,
    TYPE_IDENTIFIANT_NATIONAL_ENTREPRISE,
    IDENTIFIANT_ENTITE_JURIDIQUE,
    IDENTIFIANT_ENTREPRISE_SIEGE,
    TYPE_IDENTIFIANT_SG,
    IDENTIFIANT_ENTREPRISE_MERE,
    TYPE_IDENTIFIANT_MI,
    IDENTIFIANT_ENTREPRISE_MERE_ULTIME,
    TYPE_IDENTIFIANT_M_U,
    NOM_FAMILLE,
    PRENOM,
    TO_CHAR(DATE_NAISSANCE, 'DD/MM/YYYY'),
    ADRESSE_RUE,
    ADRESSE_NUMERO,
    ADRESSE_VILLE,
    DIVISION_ADMINISTRATIVE,
    CODE_POSTAL,
    PAYS,
    ENTITE_JURIDIQUE_NOM,
    FORME_JURIDIQUE,
    SECTEUR_INSTITUTIONNEL,
    ACTIVITE_ECONOMIQUE,
    ETAT_AVANCEMENT_PROCED_JUD,
    TO_CHAR(DATE_OUVERTURE_PROCED_JUD, 'DD/MM/YYYY'),
    TO_CHAR(TAILLE_ENTREPRISE),
    TO_CHAR(DATE_TAILLE_ENTREPRISE, 'DD/MM/YYYY'),
    TO_CHAR(NOMBRE_SALARIES),
    TO_CHAR(TOTAL_BILAN),
    TO_CHAR(CHIFFRE_AFFAIRES_ANNUEL),
    TO_CHAR(LIGNE_EXCEL),
    LIGNE_AUDIT,
    1            AS SORT_ORDER
FROM BECRIS
WHERE FEUILLE = 'CONTREPARTIE'

ORDER BY SORT_ORDER, LIGNE_EXCEL) order by num_ligne;